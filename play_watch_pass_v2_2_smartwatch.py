def conflict_tag(mid):
    conflicts = []
    for a, b, label in CONFLICT_PAIRS:
        eva = ev_max_map.get((mid,a), np.nan)
        evb = ev_max_map.get((mid,b), np.nan)
        bora = bor_max_map.get((mid,a), np.nan) if bor_max_map else np.nan
        borb = bor_max_map.get((mid,b), np.nan) if bor_max_map else np.nan

        if np.isfinite(eva) and np.isfinite(evb) and (eva > 0) and (evb > 0):
            conflicts.append(f"{label}: {a} vs {b}")
            continue

        if np.isfinite(bora) and np.isfinite(borb) and (bora >= 1.05) and (borb >= 1.05):
            conflicts.append(f"{label}(BoR): {a} vs {b}")

    if not conflicts:
        return "CONFLICT: NONE", ""
    return "CONFLICT: YES", " | ".join(conflicts[:3])

