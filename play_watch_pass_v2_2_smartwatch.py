def market_reliability(sub, mid):
    """
    Reliability uses:
    - support: positive EV rows + unique chars among those
    - BestOfRank strength for best market
    - match-level GoldenScore_MDL / MDL_BOOST
    """
    s = sub.copy()
    s.loc[:, "EVn"] = pd.to_numeric(s["EV"], errors="coerce")

    pos = s.loc[s["EVn"] > 0].copy()   # ✅ copy => no warning

    pos_rows = int(len(pos))
    pos_chars = int(pos["Karakter"].nunique()) if pos_rows else 0

    if pos_rows:
        pos.loc[:, "_p"] = pd.to_numeric(pos.get("Prob", np.nan), errors="coerce").fillna(0)
        pos = pos.sort_values(["EVn","_p"], ascending=False)
        best_market = str(pos.iloc[0]["Seçim"])
    else:
        if "BestOfRank" in s.columns:
            s2 = s.copy()
            s2.loc[:, "_bor"] = pd.to_numeric(s2["BestOfRank"], errors="coerce").fillna(-1e9)
            best_market = str(s2.sort_values("_bor", ascending=False).iloc[0]["Seçim"])
        else:
            best_market = str(s.iloc[0]["Seçim"])

    bor = bor_max_map.get((mid,best_market), np.nan) if bor_max_map else np.nan
    gs = gs_med_map.get(mid, np.nan) if gs_med_map else np.nan
    boost = boost_med_map.get(mid, np.nan) if boost_med_map else np.nan

    score = 0

    # support
    if pos_rows >= 3: score += 2
    elif pos_rows == 2: score += 1
    if pos_chars >= 2: score += 2
    elif pos_chars == 1 and pos_rows >= 2: score += 1

    # BestOfRank
    if np.isfinite(bor):
        if bor >= 1.15: score += 2
        elif bor >= 1.05: score += 1

    # MDL strength (robust threshold)
    if np.isfinite(GS_THR_75) and np.isfinite(gs) and gs >= GS_THR_75:
        score += 1

    # boost positive support
    if np.isfinite(boost) and boost > 0:
        score += 1

    if score >= 5:
        return "RELIABILITY: HIGH", best_market
    if score >= 3:
        return "RELIABILITY: MED", best_market
    return "RELIABILITY: LOW", best_market

