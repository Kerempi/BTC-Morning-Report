def verdict(sub, tags, pk):
    trap = any_nonblank(get_col(sub, "TRAP_ICON")) or any_nonblank(get_col(sub, "DANGER_ICON"))
    cr = ratio_green(get_col(sub, "CONF_ICON"))

    if trap:
        return "PASS", "Tuzak/tehlike etiketi var: burada ‘hikâye’ değil ‘risk’ baskın."

    if tags["CONFLICT_TAG"] == "CONFLICT: YES":
        if tags["EDGE_TAG"] in ("EDGE: NONE","EDGE: FRAGILE"):
            return "PASS", "Konflikt var ve edge zayıf; burada yön seçmek kumara döner."
        return "WATCH", "Konflikt var; tek bir hikâyeye kilitlenmeden izlemek daha doğru."

    if tags["EDGE_TAG"] == "EDGE: NONE":
        if tags["CORE_TAG"].startswith("CORE: STRONG"):
            return "WATCH", "Model seviyor ama fiyat kapalı. Zorlamam; izlerim."
        return "WATCH", "Hikâye var gibi ama fiyat kapısı yok."

    if cr < 0.55:
        return "PASS", "CONF dağınık; model aynı şeyi anlatmıyor."

    if tags["RELIABILITY_TAG"] == "RELIABILITY: HIGH" and tags["EDGE_TAG"] in ("EDGE: ROBUST","EDGE: OK") and tags["SAMPLE_TAG"].startswith("SAMPLE: THICK"):
        if pk:
            return "PLAY", "Hikâye güçlü; banko tarafı fiyatlanmış. Value gate’e kaymak mantıklı."
        return "PLAY", "Çekirdek + edge + reliability aynı anda geliyor; oynanabilir."

    if tags["RELIABILITY_TAG"] == "RELIABILITY: MED" and tags["CORE_TAG"].startswith("CORE: STRONG"):
        return "WATCH", "Sinyal var; ama tam ‘konfor’ için reliability bir tık daha isterim."

    return "WATCH", "Sinyal var ama ‘tam konfor’ değil; dalga payıyla izlenebilir."

def best_pick_row(sub):
    s = sub.loc[sub["_SAFE"]==True].copy()  # ✅ copy => no warning
    if len(s) == 0:
        s = sub.copy()

    if "CONF_ICON" in s.columns:
        s.loc[:, "_conf"] = (s["CONF_ICON"]=="🟢").astype(int)
    else:
        s.loc[:, "_conf"] = 0

    if "AUTOMOD_ICON" in s.columns:
        s.loc[:, "_am"] = (s["AUTOMOD_ICON"]=="🟢").astype(int)
    else:
        s.loc[:, "_am"] = 0

    s.loc[:, "_ev"]  = pd.to_numeric(s["EV"], errors="coerce").fillna(-1e9)
    s.loc[:, "_p"]   = pd.to_numeric(s["Prob"], errors="coerce").fillna(0)

    if "BestOfRank" in s.columns:
        s.loc[:, "_bor"] = pd.to_numeric(s["BestOfRank"], errors="coerce").fillna(0)
    else:
        s.loc[:, "_bor"] = 0

    s = s.sort_values(["_conf","_am","_ev","_bor","_p"], ascending=False)
    return s.iloc[0]

