def triangle_price_killed(mid, triangles_exist):
    if not triangles_exist:
        return (False, "", "")

    banko_evs = [ev_max_map.get((mid,m), np.nan) for m in BANKO_MARKETS]
    goal_evs  = [ev_max_map.get((mid,m), np.nan) for m in GOAL_MARKETS]
    val_evs   = [(m, ev_max_map.get((mid,m), np.nan)) for m in VALUE_GATES]

    banko_evs = [float(x) for x in banko_evs if np.isfinite(x)]
    goal_evs  = [float(x) for x in goal_evs if np.isfinite(x)]
    val_evs   = [(m,float(v)) for (m,v) in val_evs if np.isfinite(v)]

    banko_killed = (len(banko_evs)>0) and (max(banko_evs) <= 0)
    goal_killed  = (len(goal_evs)>0)  and (max(goal_evs)  <= 0)
    value_gate   = (len(val_evs)>0) and (max([v for _,v in val_evs]) > 0)

    killed = (banko_killed or goal_killed) and value_gate
    gate = ""
    if killed:
        gate = sorted(val_evs, key=lambda t: t[1], reverse=True)[0][0]

    detail = []
    if banko_killed: detail.append("Banko tarafı fiyatlanmış")
    if goal_killed:  detail.append("Gol marketi fiyatlanmış")
    return killed, gate, " + ".join(detail)

