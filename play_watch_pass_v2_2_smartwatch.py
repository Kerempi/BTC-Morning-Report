def core_tag(sub):
    mdl_rank = med(get_col(sub, "MDL_rank"))
    q = med(get_col(sub, "MDL_quantile"))
    cr = ratio_green(get_col(sub, "CONF_ICON"))
    ar = ratio_green(get_col(sub, "AUTOMOD_ICON"))

    if np.isfinite(mdl_rank) and mdl_rank <= 1 and np.isfinite(q) and q >= 0.75 and cr >= 0.70:
        return "CORE: STRONG" if ar >= 0.40 else "CORE: STRONG-ish"
    if cr >= 0.60 and (np.isfinite(q) and q >= 0.60):
        return "CORE: OK"
    return "CORE: FRAGILE"

def sample_tag(sub):
    eff = med(get_col(sub, "EFFECTIVE_N"))
    pen = med(get_col(sub, "_EN_PEN"))
    if not np.isfinite(pen):
        pen = 0.0

    if np.isfinite(eff) and eff >= 40:
        return "SAMPLE: THICK (PEN)" if pen >= 3 else "SAMPLE: THICK"
    if np.isfinite(eff) and eff >= 30:
        return "SAMPLE: OK (PEN)" if pen >= 3 else "SAMPLE: OK"
    if np.isfinite(eff) and eff >= 20:
        return "SAMPLE: THIN"
    return "SAMPLE: VERY THIN"

def edge_tag(sub):
    s = sub.copy()
    s.loc[:, "EVn"] = pd.to_numeric(s["EV"], errors="coerce")
    pos = s.loc[s["EVn"] > 0]
    if len(pos) == 0:
        return "EDGE: NONE"
    pos_rows = int(len(pos))
    pos_chars = int(pos["Karakter"].nunique())
    if pos_rows >= 3 and pos_chars >= 2:
        return "EDGE: ROBUST"
    if pos_rows >= 2:
        return "EDGE: OK"
    return "EDGE: FRAGILE"

def consistency_tag(sub):
    sicon = get_col(sub, "STRENGTH_ICON")
    scat = get_col(sub, "AnchorStrengthCategory")
    drift = False
    if sicon is not None and sicon.astype(str).str.strip().nunique() >= 2:
        drift = True
    if scat is not None and scat.astype(str).str.strip().nunique() >= 2:
        drift = True
    return "CONSISTENCY: DRIFT" if drift else "CONSISTENCY: CLEAN"

def arch_tag(sub):
    arch = get_col(sub, "ARCH_ICON")
    if arch is None:
        return "ARCH: N/A"
    uniq = arch.astype(str).str.strip()
    uniq = uniq[uniq != ""]
    if len(uniq) == 0:
        return "ARCH: N/A"
    n = uniq.nunique()
    if n == 1: return "ARCH: FOCUSED"
    if n == 2: return "ARCH: MIXED"
    return "ARCH: NOISY"

