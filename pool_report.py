def build_coupons(best_df, match_cache,
                  banko_n=5, surpriz_n=4, system_n=8, kombo_n=6):

    def unique_by_match(picks):
        seen = set()
        out = []
        for p in picks:
            if p["Match_ID"] in seen:
                continue
            seen.add(p["Match_ID"])
            out.append(p)
        return out

    base = []
    for _, r in best_df.iterrows():
        base.append({
            "Match_ID": r["Match_ID"],
            "Match_Label": r["Match_Label"],
            "Pick": r["Decision_Market"],
            "Odd": r["Decision_Odd"],
            "Prob": r["Decision_Prob"],
            "EV": r["Decision_EV"],
            "FinalScore": r["Decision_FinalScore"],
            "Trust": r["CORE_SCORE"],
            "Conf": r["CONF_ICON"],
            "Auto": r["AUTOMOD_ICON"],
            "Rows": r["Rows"]
        })

    # --- BANKO
    banko = [p for p in base
             if safe_str(p["Pick"]).strip() != ""
             and pd.notna(p["Prob"]) and float(p["Prob"]) >= 0.62
             and pd.notna(p["Odd"]) and float(p["Odd"]) <= 1.65
             and pd.notna(p["FinalScore"]) and float(p["FinalScore"]) > -0.02
             and int(p["Trust"]) >= 70
             and p["Conf"] in ("🟢","🟡")]
    banko = sorted(banko, key=lambda x: (x["Trust"], float(x["Prob"]), -float(x["Odd"])), reverse=True)
    banko = unique_by_match(banko)[:banko_n]

    # --- SURPRIZ
    surpriz = [p for p in base
               if safe_str(p["Pick"]).strip() != ""
               and pd.notna(p["Odd"]) and float(p["Odd"]) >= 2.10
               and pd.notna(p["FinalScore"]) and float(p["FinalScore"]) > 0.00
               and int(p["Trust"]) >= 55]
    surpriz = sorted(surpriz, key=lambda x: (float(x["FinalScore"]), float(x["Odd"])), reverse=True)
    surpriz = unique_by_match(surpriz)[:surpriz_n]

    # --- SYSTEM
    system = [p for p in base
              if safe_str(p["Pick"]).strip() != ""
              and pd.notna(p["Odd"]) and 1.70 <= float(p["Odd"]) <= 3.50
              and pd.notna(p["FinalScore"]) and float(p["FinalScore"]) > 0.00
              and int(p["Trust"]) >= 60]
    system = sorted(system, key=lambda x: (float(x["FinalScore"]), x["Trust"]), reverse=True)
    system = unique_by_match(system)[:system_n]

    # --- KOMBO (FIXED)
    combo_candidates = []

    def synergy(a: str, b: str) -> float:
        A, B = (a or "").strip(), (b or "").strip()
        pos = {
            ("MS 1","2.5 Üst"): 0.10, ("2.5 Üst","MS 1"): 0.10,
            ("MS 1","KG Var"):  0.10, ("KG Var","MS 1"):  0.10,
            ("MS 2","2.5 Üst"): 0.10, ("2.5 Üst","MS 2"): 0.10,
            ("MS 2","KG Var"):  0.10, ("KG Var","MS 2"):  0.10,
            ("MSX","2.5 Alt"):  0.12, ("2.5 Alt","MSX"):  0.12,
            ("MSX","KG Yok"):   0.12, ("KG Yok","MSX"):   0.12,
        }
        neg = {
            ("MS 1","2.5 Alt"): -0.10, ("2.5 Alt","MS 1"): -0.10,
            ("MS 1","KG Yok"):  -0.10, ("KG Yok","MS 1"):  -0.10,
            ("MS 2","2.5 Alt"): -0.08, ("2.5 Alt","MS 2"): -0.08,
            ("MS 2","KG Yok"):  -0.08, ("KG Yok","MS 2"):  -0.08,
        }
        return pos.get((A,B), 0.0) + neg.get((A,B), 0.0)

    def ms1_low_odd_penalty(market: str, odd: float) -> float:
        if (market or "").strip() == "MS 1" and pd.notna(odd) and float(odd) <= 1.30:
            return 0.10
        return 0.0

    def market_type(name: str) -> str:
        n = (name or "").strip()
        if n in ("MS 1","MS 2","MSX"): return "MS"
        if "Alt" in n or "Üst" in n or "Over" in n or "Under" in n: return "OU"
        if "KG" in n or "BTTS" in n: return "BTTS"
        return "OTHER"

    # ✅ İstersen False yap: OU+OU veya BTTS+BTTS gibi ikilileri engeller
    ALLOW_SAME_TYPE = True

    for mid, info in match_cache.items():
        scored = info["scored"]
        core = info["core"]
        if not scored:
            continue

        trust100 = int(round(core["TRUST"]*100))
        if trust100 < 60:
            continue

        # (A) Market-level unique (same market appears many rows → keep best FinalScore row)
        best_by_market = {}
        for m in scored:
            mk = safe_str(m.get("Market","")).strip()
            if mk == "":
                continue
            cur = best_by_market.get(mk)
            if (cur is None) or (float(m.get("FinalScore",-999)) > float(cur.get("FinalScore",-999))):
                best_by_market[mk] = m

        market_list = list(best_by_market.values())
        if len(market_list) < 2:
            continue

        # (B) Top-K markets (not rows)
        K = 5
        top = sorted(market_list, key=lambda x: (x.get("FinalScore",-999), x.get("EV",-999)), reverse=True)[:K]
        if len(top) < 2:
            continue

        best_for_match = None

        # (C) all distinct pairs
        for i in range(len(top)):
            for j in range(i+1, len(top)):
                a, b = top[i], top[j]
                ma, mb = safe_str(a.get("Market","")).strip(), safe_str(b.get("Market","")).strip()

                # ✅ hard rule: no same market twice
                if ma == mb:
                    continue

                if (not ALLOW_SAME_TYPE) and (market_type(ma) == market_type(mb)):
                    continue

                oa, ob = a.get("Odd", np.nan), b.get("Odd", np.nan)
                pa, pb = a.get("Prob", np.nan), b.get("Prob", np.nan)
                ea, eb = a.get("EV", np.nan), b.get("EV", np.nan)

                if pd.isna(oa) or pd.isna(ob):
                    continue

                combo_odd = float(oa) * float(ob)
                if combo_odd < 2.10:
                    continue

                if priced_in_flag(pa, ea) and priced_in_flag(pb, eb):
                    continue

                fsa = float(a.get("FinalScore",-1))
                fsb = float(b.get("FinalScore",-1))
                if max(fsa, fsb) <= 0.00:
                    continue

                combo_prob = (float(pa) if pd.notna(pa) else 0.5) * (float(pb) if pd.notna(pb) else 0.5)

                syn = synergy(ma, mb)
                pen = ms1_low_odd_penalty(ma, oa) + ms1_low_odd_penalty(mb, ob)

                cons_min = min(float(a.get("Consensus",0.35)), float(b.get("Consensus",0.35)))
                combo_score = ((fsa + fsb)/2.0) + 0.15*core["TRUST"] + syn + 0.08*cons_min - pen

                cand = {
                    "Match_ID": mid,
                    "Match_Label": info["label"],
                    "LegA": ma, "OddA": oa, "ProbA": pa, "EVA": ea,
                    "LegB": mb, "OddB": ob, "ProbB": pb, "EVB": eb,
                    "Combo_Odd": combo_odd,
                    "Combo_Prob": combo_prob,
                    "Combo_Score": combo_score,
                    "Trust": trust100,
                    "Conf": core["CONF_ICON"],
                    "Auto": core["AUTOMOD_ICON"],
                }

                if (best_for_match is None) or (cand["Combo_Score"] > best_for_match["Combo_Score"]):
                    best_for_match = cand

        if best_for_match is not None:
            combo_candidates.append(best_for_match)

    combo_candidates = sorted(combo_candidates, key=lambda x: (x["Combo_Score"], x["Combo_Odd"]), reverse=True)
    combo = combo_candidates[:kombo_n]

    return {
        "banko": banko,
        "surpriz": surpriz,
        "system": system,
        "combo": combo,
    }

