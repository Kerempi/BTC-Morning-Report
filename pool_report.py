def summarize_core(block: pd.DataFrame):
    mdl_rank = mode_or_blank(block["MDL_rank"]) if "MDL_rank" in block.columns else ""
    qmin = _nanmin(block["MDL_quantile"]) if "MDL_quantile" in block.columns else np.nan
    qmax = _nanmax(block["MDL_quantile"]) if "MDL_quantile" in block.columns else np.nan
    anchor = _nanmean(block["SIM_ANCHOR_STRENGTH"]) if "SIM_ANCHOR_STRENGTH" in block.columns else np.nan
    effn   = _nanmean(block["EFFECTIVE_N"]) if "EFFECTIVE_N" in block.columns else np.nan
    enpen  = _nanmean(block["_EN_PEN"]) if "_EN_PEN" in block.columns else np.nan

    conf = icon_mode(block["CONF_ICON"]) if "CONF_ICON" in block.columns else ""
    automod = icon_mode(block["AUTOMOD_ICON"]) if "AUTOMOD_ICON" in block.columns else ""
    trap = icon_mode(block["TRAP_ICON"]) if "TRAP_ICON" in block.columns else ""
    danger = icon_mode(block["DANGER_ICON"]) if "DANGER_ICON" in block.columns else ""

    strength_dist = dict(Counter(block["STRENGTH_ICON"].fillna("").astype(str))) if "STRENGTH_ICON" in block.columns else {}
    cat_dist = dict(Counter(block["AnchorStrengthCategory"].fillna("").astype(str))) if "AnchorStrengthCategory" in block.columns else {}

    score = 0.0
    try:
        r = int(mdl_rank)
        score += 0.25 if r == 1 else (0.18 if r == 2 else 0.12)
    except:
        score += 0.14

    if not np.isnan(qmax): score += 0.22 * float(np.clip(qmax, 0, 1))
    if not np.isnan(anchor): score += 0.18 * float(np.clip((anchor - 0.30) / 0.20, 0, 1))
    if not np.isnan(effn): score += 0.22 * float(np.clip((effn - 15) / 35, 0, 1))

    if conf == "🟢": score += 0.07
    elif conf == "🟡": score += 0.04

    if automod == "🟢": score += 0.06
    elif automod == "🟡": score += 0.03

    penalty = 0.0
    if trap not in ("", "nan"): penalty += 0.10
    if danger not in ("", "nan"): penalty += 0.18

    def mix_pen(dist):
        if not dist: return 0.0
        total = sum(dist.values())
        if total <= 1: return 0.0
        top = max(dist.values())
        purity = top / total
        return float(np.clip((0.60 - purity) / 0.60, 0, 1)) * 0.06

    penalty += mix_pen(strength_dist)
    penalty += mix_pen(cat_dist)

    trust = float(np.clip(score - penalty, 0.0, 1.0))
    return {
        "MDL_rank": mdl_rank,
        "MDL_qmin": qmin,
        "MDL_qmax": qmax,
        "Anchor_mean": anchor,
        "EFFECTIVE_N_mean": effn,
        "_EN_PEN_mean": enpen,
        "CONF_ICON": conf,
        "AUTOMOD_ICON": automod,
        "TRAP_ICON": trap,
        "DANGER_ICON": danger,
        "STRENGTH_DIST": strength_dist,
        "CAT_DIST": cat_dist,
        "TRUST": trust,
        "TRUST_100": int(round(trust*100)),
    }

