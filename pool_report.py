def market_consensus(block: pd.DataFrame):
    if "Seçim" not in block.columns: return {}
    total = len(block)
    if total == 0: return {}

    cons = {}
    for market, g in block.groupby("Seçim"):
        mk = safe_str(market).strip()
        if mk == "": continue
        share = len(g) / total
        if ("CONF_ICON" in g.columns and "AUTOMOD_ICON" in g.columns):
            green = int(((g["CONF_ICON"]=="🟢") & (g["AUTOMOD_ICON"]=="🟢")).sum())
        else:
            green = 0
        green_share = green / len(g) if len(g) else 0.0
        effn_mean = _nanmean(g["EFFECTIVE_N"]) if "EFFECTIVE_N" in g.columns else np.nan
        cons[mk] = {"share": share, "green_share": green_share, "effn_mean": effn_mean, "n": len(g)}

    effns = [v["effn_mean"] for v in cons.values() if not np.isnan(v["effn_mean"])]
    if len(effns):
        mn, mx = float(np.nanmin(effns)), float(np.nanmax(effns))
        for k in cons:
            e = cons[k]["effn_mean"]
            cons[k]["effn_norm"] = 0.5 if (np.isnan(e) or mx == mn) else float(np.clip((e - mn)/(mx-mn),0,1))
    else:
        for k in cons:
            cons[k]["effn_norm"] = 0.5

    for k, v in cons.items():
        v["cons_score"] = float(np.clip(0.55*v["share"] + 0.30*v["green_share"] + 0.15*v["effn_norm"], 0, 1))
    return cons

def score_markets_for_match(core, block):
    markets = summarize_markets(block)
    cons_map = market_consensus(block)
    ev_z = robust_z(pd.Series([m["EV"] for m in markets], dtype="float"), clip=2.5)

    scored = []
    for i, m in enumerate(markets):
        mk = safe_str(m.get("Market","")).strip()
        if mk == "":
            continue

        evz = float(ev_z.iloc[i]) if i in ev_z.index else 0.0
        ev_component = evz / 2.5  # ~ -1..1

        cons_score = cons_map.get(mk, {}).get("cons_score", 0.35)
        green_share = cons_map.get(mk, {}).get("green_share", 0.0)

        pen = 0.0
        if core["TRAP_ICON"] not in ("", "nan"): pen += 0.20
        if core["DANGER_ICON"] not in ("", "nan"): pen += 0.35
        if core["AUTOMOD_ICON"] == "🟡": pen += 0.05
        if core["AUTOMOD_ICON"] == "🔴": pen += 0.10
        if green_share < 0.25: pen += 0.06
        if priced_in_flag(m.get("Prob", np.nan), m.get("EV", np.nan)): pen += 0.12

        scale = (0.55 + 0.45*core["TRUST"]) * (0.60 + 0.40*cons_score)
        final = (ev_component * scale) - pen

        scored.append({
            **m,
            "EV_Z": evz,
            "Consensus": cons_score,
            "GreenShare": green_share,
            "FinalScore": final,
            "Penalty": pen
        })

    scored = sorted(
        scored,
        key=lambda x: (
            x.get("FinalScore", -999),
            float(x.get("EV", -999)) if pd.notna(x.get("EV")) else -999,
            float(x.get("Prob", -999)) if pd.notna(x.get("Prob")) else -999,
        ),
        reverse=True
    )
    return scored, cons_map

def pick_decision_smart(core, block):
    scored, cons = score_markets_for_match(core, block)
    if not scored:
        return None, ["Yeterli market verisi yok."], cons

    best = scored[0]
    reasons = []
    if core["CONF_ICON"] == "🟢": reasons.append("CONF 🟢 → model hikâyeyi destekliyor")
    if core["AUTOMOD_ICON"] == "🟢": reasons.append("AutoMod 🟢 → model mutabakatı güçlü")
    elif core["AUTOMOD_ICON"] == "🟡": reasons.append("AutoMod 🟡 → tam mutabakat yok (dalga payı)")

    if pd.notna(core["EFFECTIVE_N_mean"]) and core["EFFECTIVE_N_mean"] >= 30:
        reasons.append(f"EFFECTIVE_N {fmt(core['EFFECTIVE_N_mean'],2)} → örneklem güçlü")
    if pd.notna(core["Anchor_mean"]):
        reasons.append(f"Anchor {fmt(core['Anchor_mean'],3)} → yapısal omurga seviyesi")

    reasons.append(f"Market konsensüsü: {fmt(best['Consensus'],2)}")
    reasons.append(f"FinalScore: {fmt(best['FinalScore'],3)} (EV + güven + konsensüs)")

    if priced_in_flag(best.get("Prob", np.nan), best.get("EV", np.nan)):
        reasons.append("Not: Prob yüksek ama EV negatif → market fiyatlanmış olabilir (priced-in).")
    if core["TRAP_ICON"] not in ("", "nan") or core["DANGER_ICON"] not in ("", "nan"):
        reasons.append("Uyarı: TRAP/DANGER etiketi var → temkinli.")

    return best, reasons, cons

